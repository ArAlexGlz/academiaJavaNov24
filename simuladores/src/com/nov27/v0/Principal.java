package com.nov27.v0;

public class Principal {

	public static void main(String[] args) {

		int iDec = 11;
		System.out.println("Decimal: "+iDec);
		
		int iBin = 0b11; //0B
		System.out.println("Binario: "+iBin);
		
		int iOct = 07; 
		System.out.println("Octal: "+iOct);
		
		int iHex = 0x11; //0X 
		System.out.println("Hexadecimal: "+iHex);
		
		int iHex1 = 0XF; //0X 
		System.out.println("Hexadecimal: "+iHex1);

		
	}

}
